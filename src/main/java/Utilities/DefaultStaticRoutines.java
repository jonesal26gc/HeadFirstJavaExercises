public class DefaultStaticRoutines {

    public static int largerOne(int a, int b){

        if (a>b) {
            return a;
        } else {
            return b;
        }
    }

    public static  int smallerOne(int a, int b){
        if (a<b) {
            return a;
        } else {
            return b;
        }
    }
}
